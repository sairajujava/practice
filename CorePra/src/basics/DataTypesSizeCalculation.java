package basics;

public class DataTypesSizeCalculation {

	public static void main(String[] args) {
		
		
		System.out.println("Byte SIZE : "+Byte.SIZE/8 + "MinValue :"+Byte.MIN_VALUE + "MAX_VALUE:" +Byte.MAX_VALUE);
		
		System.out.println("Integer SIZE : "+Integer.SIZE/8 + "MinValue :"+Integer.MIN_VALUE + "MAX_VALUE:" +Integer.MAX_VALUE);
		
		System.out.println("Long SIZE : "+Long.SIZE/8 + "MinValue :"+Long.MIN_VALUE + "MAX_VALUE:" +Long.MAX_VALUE);
		
		System.out.println("Short SIZE : "+Short.SIZE/8 + "MinValue :"+Short.MIN_VALUE + "MAX_VALUE:" +Short.MAX_VALUE);
		
		System.out.println("Char SIZE : "+Character.SIZE/8);
		
		System.out.println("Float SIZE : "+Float.SIZE/8 + "MinValue :"+Float.MIN_VALUE + "MAX_VALUE:" +Float.MAX_VALUE);
		
		System.out.println("double SIZE : "+Double.SIZE/8 + "MinValue :"+Double.MIN_VALUE + "MAX_VALUE:" +Double.MAX_VALUE);

	}

}
